# 트리를 구현한 모듈들을 임포트합니다.
# (동일 디렉토리에 btree.py, bstar_tree.py, bplus_tree.py가 있다고 가정)
from btree import BTree
from bstar_tree import BStarTree
from bplus_tree import BPlusTree

def load_data(filepath):
    """
    CSV 파일에서 100,000개의 학생 데이터를 읽어옵니다.
    배열 인덱스가 RID가 되고, 학번(Student ID)이 Key가 됩니다[cite: 39].
    """
    keys = []
    # 파일이 없을 경우 테스트를 위한 더미 데이터 생성 로직
    if not os.path.exists(filepath):
        print(f"[경고] {filepath} 파일이 없습니다. 임시 더미 데이터 100,000개를 생성합니다.")
        base_id = 20200000
        keys = random.sample(range(base_id, base_id + 500000), 100000)
        return keys

    with open(filepath, mode='r', encoding='utf-8') as f:
        reader = csv.reader(f)
        next(reader, None)  # 헤더 스킵
        for row in reader:
            if row:
                # 첫 번째 컬럼이 학번이라고 가정 [cite: 37]
                student_id = int(row[0])
                keys.append(student_id)
    return keys

def btree_range_search(node, start_key, end_key, result):
    """B-tree와 B*-tree를 위한 중위 순회(In-order) 기반의 범위 검색 헬퍼 함수입니다."""
    if not node:
        return
    i = 0
    while i < len(node.keys) and node.keys[i] < start_key:
        i += 1
    while i < len(node.keys) and node.keys[i] <= end_key:
        if not node.is_leaf:
            btree_range_search(node.children[i], start_key, end_key, result)
        result.append((node.keys[i], node.rids[i]))
        i += 1
    if not node.is_leaf:
        btree_range_search(node.children[i], start_key, end_key, result)

def measure_workloads(order, data_keys):
    """명세서에 명시된 4가지 фундаменталь 실험을 수행합니다."""
    print(f"\n{'='*50}")
    print(f" EXPERIMENT START: Tree Order (d) = {order}")
    print(f"{'='*50}")

    trees = {
        "B-Tree": BTree(order),
        "B*-Tree": BStarTree(order),
        "B+-Tree": BPlusTree(order)
    }

    # ---------------------------------------------------------
    # 1. Insertion & Parameter Tuning [cite: 42, 43]
    # ---------------------------------------------------------
    print("\n[1] Workload: Insertion (100,000 records)")
    for name, tree in trees.items():
        start_time = time.perf_counter()
        for rid, key in enumerate(data_keys):
            tree.insert(key, rid)
        end_time = time.perf_counter()

        # B*-tree는 재분배 횟수도 함께 출력
        extra_info = f", Redistributions: {tree.redistribute_count}" if name == "B*-Tree" else ""
        print(f"  - {name:8s}: {end_time - start_time:.4f} sec | Splits: {tree.split_count}{extra_info}")

    # ---------------------------------------------------------
    # 2. Point Search [cite: 45, 46]
    # ---------------------------------------------------------
    search_sample_size = 10000
    search_keys = random.sample(data_keys, search_sample_size)
    print(f"\n[2] Workload: Point Search ({search_sample_size} random keys)")
    for name, tree in trees.items():
        start_time = time.perf_counter()
        for key in search_keys:
            tree.search(key)
        end_time = time.perf_counter()
        avg_time = (end_time - start_time) / search_sample_size * 1_000_000  # 마이크로초 단위
        print(f"  - {name:8s}: {end_time - start_time:.4f} sec (Avg: {avg_time:.2f} µs/query)")

    # ---------------------------------------------------------
    # 3. Range Query [cite: 47, 49]
    # ---------------------------------------------------------
    # 임의의 범위 설정 (예: 20210000 ~ 20215000)
    range_start, range_end = 20210000, 20215000
    print(f"\n[3] Workload: Range Query (Keys {range_start} to {range_end})")

    for name, tree in trees.items():
        start_time = time.perf_counter()
        result = []
        if name == "B+-Tree":
            result = tree.range_query(range_start, range_end)
        else:
            btree_range_search(tree.root, range_start, range_end, result)
        end_time = time.perf_counter()
        print(f"  - {name:8s}: {end_time - start_time:.6f} sec | Found records: {len(result)}")

    # ---------------------------------------------------------
    # 4. Deletion & Structural Integrity [cite: 50, 52]
    # ---------------------------------------------------------
    # 10% 무작위 삭제 [cite: 51]
    delete_sample_size = len(data_keys) // 10
    delete_keys = random.sample(data_keys, delete_sample_size)
    print(f"\n[4] Workload: Deletion ({delete_sample_size} random keys - 10%)")

    for name, tree in trees.items():
        start_time = time.perf_counter()
        for key in delete_keys:
            tree.delete(key)
        end_time = time.perf_counter()
        print(f"  - {name:8s}: {end_time - start_time:.4f} sec")


def run_edge_case(order):
    """
    [보너스 포인트] 엣지 케이스 실험: 순차 삽입 (Sequential Insertion) [cite: 70, 71]
    트리 구조의 약점을 파고드는 정렬된 데이터 삽입 테스트.
    """
    print(f"\n{'='*50}")
    print(f" ADDITIONAL EXPERIMENT: Sequential Insertion (d={order})")
    print(f"{'='*50}")

    # 10만 개의 완전히 정렬된 학번
    seq_keys = list(range(20200000, 20200000 + 100000))

    trees = {
        "B-Tree": BTree(order),
        "B*-Tree": BStarTree(order),
    }

    for name, tree in trees.items():
        start_time = time.perf_counter()
        for rid, key in enumerate(seq_keys):
            tree.insert(key, rid)
        end_time = time.perf_counter()

        print(f"  - {name:8s} | Time: {end_time - start_time:.4f} sec | Splits: {tree.split_count}")
        if name == "B*-Tree":
            print(f"             | Redistributions: {tree.redistribute_count}")
            print(f"             | * Analysis: B*-tree effectively delays splits even in worst-case.")

